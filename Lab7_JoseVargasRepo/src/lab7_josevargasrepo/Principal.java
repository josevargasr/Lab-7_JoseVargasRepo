    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        boolean login = false;
        if (rb_user.isSelected()) {
            adminUsuario ac = new adminUsuario("./usuarios.jfv");
            ac.cargarArchivo();
            for (Usuario u : ac.getListausuarios()) {
                if (u.getUsuario().equals(tf_user.getText()) && u.getPassword().equals(pf_pass.getText())) {
                    login = true;
                    iniciado = u;
                    JOptionPane.showMessageDialog(jd_login, "Se inició sesión con éxito!");
                    jMenu1.setEnabled(true);
                    jMenu2.setEnabled(false);
                    mi_logout.setEnabled(true);
                    mi_login.setEnabled(false);
                    mi_rega.setEnabled(false);
                    mi_regu.setEnabled(false);
                }
            }
        } else {
            adminArtista ac = new adminArtista("./artistas.jfv");
            ac.cargarArchivo();
            for (Artista u : ac.getListaartistas()) {
                if (u.getUsuario().equals(tf_user.getText()) && u.getPassword().equals(pf_pass.getText())) {
                    ainiciado = u;
                    login = true;
                    JOptionPane.showMessageDialog(jd_login, "Se inició sesión con éxito!");
                    jMenu2.setEnabled(true);
                    jMenu1.setEnabled(false);
                    mi_logout.setEnabled(true);
                    mi_login.setEnabled(false);
                    mi_rega.setEnabled(false);
                    mi_regu.setEnabled(false);
                }
            }
        }
        if (login) {
            jd_login.dispose();
        } else {
            JOptionPane.showMessageDialog(jd_login, "El usuario o contraseña es incorrecto");
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        try {
            Usuario u = new Usuario(tf_nombreu.getText(), tf_apellidou.getText(), tf_usuariou.getText(), tf_passu.getText(), dc_fechau.getDate());
            adminUsuario ac = new adminUsuario("./usuarios.jfv");
            ac.cargarArchivo();
            ac.getListausuarios().add(u);
            ac.escribirArchivo();
            JOptionPane.showMessageDialog(this, "Usuario guardado exitosamente");
            jd_crearu.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar el usuario");
        }

    }//GEN-LAST:event_jButton2MouseClicked

